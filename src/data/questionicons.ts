export function resolveQuestionIcon(
    iconUrl: string | null | undefined,
    statement: string,
): string | null {
    // 1. Always use stored icon first (from DB or prediction engine image_url)
    if (iconUrl) return iconUrl;

    // 2. Keyword match — longer keywords checked first to avoid false positives
    const lower = statement.toLowerCase();
    for (const [keywords, url] of KEYWORD_ICONS) {
        if (keywords.some(k => lower.includes(k))) return url;
    }

    // 3. No match — QuestionIcon component will show letter fallback
    return null;
}
